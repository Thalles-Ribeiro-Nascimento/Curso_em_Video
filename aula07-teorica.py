"""
Operadores aritméticos
"""
# Ordem de precedência = a
a = 5 * 3 + 4 ** 2
b = 5 - 2
c = 5 * 2
d = 5 / 2
e = 5 ** 2
f = 5 // 2
g = 5 % 2

print(f"5 x 3 + 4² = {a}\n"
      f"5 - 2 = {b}\n"
      f"5 x 2 = {c}\n"
      f"5 / 2 = {d}\n"
      f"5² = {e}\n"
      f"Divisão inteira de 5 por 2 = {f}\n"
      f"Resto da divisão de 5 por 2 = {g}\n"
      f"Fim")
